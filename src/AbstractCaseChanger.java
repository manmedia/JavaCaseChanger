/**
 * A simple interface description change cases of all the Alphanumeric
 * characters in a file.
 * 
 * 
 * @author manna
 * 
 */

public abstract class AbstractCaseChanger {

	/**
	 * processor method
	 * This method is flexible and will be implemented by an 
	 * appropriate child class.
	 * 
	 * @param none
	 * @return none
	 * 
	 */

	public abstract void processFiles();
}
